
public class Add2Num {

	public static void main(String[] args) {
// for integer
		int a=10;
		int b=20;
		int c=a+b;
//for float
		double p=1.234;
		double q=1.334;
		double r=p+q;
//for character
		char x='a';
		char y='b';
		String z=Character.toString(x)+Character.toString(y);
		System.out.println(c);
		System.out.println(r);
		System.out.println(z);
	}
}
